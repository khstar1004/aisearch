class ProbeHtmlParser(HTMLParser):
    def __init__(self) -> None:
        super().__init__(convert_charrefs=True)
        self.elements: list[dict[str, Any]] = []
        self.stack: list[int] = []

    def handle_starttag(self, tag: str, attrs: list[tuple[str, str | None]]) -> None:
        normalized = tag.lower()
        parent = self.stack[-1] if self.stack else None
        element = {
            "index": len(self.elements),
            "tag": normalized,
            "attrs": {str(name).lower(): value or "" for name, value in attrs},
            "parent": parent,
            "children": [],
            "text": "",
        }
        self.elements.append(element)
        if parent is not None:
            self.elements[parent]["children"].append(element["index"])
        if normalized not in VOID_TAGS:
            self.stack.append(element["index"])

    def handle_startendtag(self, tag: str, attrs: list[tuple[str, str | None]]) -> None:
        self.handle_starttag(tag, attrs)
        if self.stack and self.elements[self.stack[-1]]["tag"] == tag.lower():
            self.stack.pop()

    def handle_endtag(self, tag: str) -> None:
        normalized = tag.lower()
        while self.stack:
            index = self.stack.pop()
            if self.elements[index]["tag"] == normalized:
                break

    def handle_data(self, data: str) -> None:
        if self.stack:
            self.elements[self.stack[-1]]["text"] += data


def parse_html(body: str) -> list[dict[str, Any]]:
    parser = ProbeHtmlParser()
    parser.feed(body or "")
    return parser.elements


def attr(element: dict[str, Any], name: str) -> str:
    return str(element.get("attrs", {}).get(name.lower()) or "").strip()


def has_attr(element: dict[str, Any], name: str) -> bool:
    return name.lower() in (element.get("attrs") or {})


def style_declares_hidden(style: str) -> bool:
    normalized = re.sub(r"\s+", "", str(style or "").lower())
    return "display:none" in normalized or "visibility:hidden" in normalized or "visibility:collapse" in normalized


def element_has_hidden_class(element: dict[str, Any]) -> bool:
    hidden_classes = {"hidden", "hide", "d-none", "display-none", "sr-only", "visually-hidden", "screen-reader-text"}
    classes = {value.strip().lower() for value in attr(element, "class").split() if value.strip()}
    return bool(classes & hidden_classes)


def element_is_hidden(elements: list[dict[str, Any]], element: dict[str, Any]) -> bool:
    current: dict[str, Any] | None = element
    while current is not None:
        if has_attr(current, "hidden") or attr(current, "aria-hidden").lower() == "true":
            return True
        if style_declares_hidden(attr(current, "style")):
            return True
        if element_has_hidden_class(current):
            return True
        parent = current.get("parent")
        current = elements[parent] if parent is not None else None
    return False


def element_is_disabled_or_readonly(elements: list[dict[str, Any]], element: dict[str, Any]) -> bool:
    if has_attr(element, "disabled") or has_attr(element, "readonly") or attr(element, "aria-disabled").lower() == "true":
        return True
    current: dict[str, Any] | None = element
    while current is not None:
        if current.get("tag") == "fieldset" and has_attr(current, "disabled"):
            return True
        parent = current.get("parent")
        current = elements[parent] if parent is not None else None
    return False


def element_text(elements: list[dict[str, Any]], index: int) -> str:
    element = elements[index]
    parts = [str(element.get("text") or "")]
    for child in element.get("children", []):
        parts.append(element_text(elements, child))
    return " ".join(part.strip() for part in parts if part.strip())


def ancestor(elements: list[dict[str, Any]], index: int, tag: str) -> dict[str, Any] | None:
    parent = elements[index].get("parent")
    while parent is not None:
        candidate = elements[parent]
        if candidate.get("tag") == tag:
            return candidate
        parent = candidate.get("parent")
    return None


def descendants(elements: list[dict[str, Any]], index: int) -> list[dict[str, Any]]:
    found: list[dict[str, Any]] = []
    for child in elements[index].get("children", []):
        found.append(elements[child])
        found.extend(descendants(elements, child))
    return found


def haystack(element: dict[str, Any], extra: str = "") -> str:
    attrs = element.get("attrs", {})
    values = [
        element.get("tag", ""),
        attrs.get("id", ""),
        attrs.get("name", ""),
        attrs.get("class", ""),
        attrs.get("placeholder", ""),
        attrs.get("title", ""),
        attrs.get("aria-label", ""),
        attrs.get("value", ""),
        extra,
    ]
    return " ".join(str(value or "").lower() for value in values)


def contains_search_term(value: str) -> bool:
    lowered = str(value or "").lower()
    return any(term in lowered for term in SEARCH_TERMS)


def input_score(elements: list[dict[str, Any]], element: dict[str, Any]) -> int:
    if element.get("tag") not in {"input", "textarea"}:
        return 0
    input_type = attr(element, "type").lower() or "text"
    if input_type in BAD_INPUT_TYPES:
        return 0
    if element_is_hidden(elements, element) or element_is_disabled_or_readonly(elements, element):
        return 0
    score = 1
    if input_type == "search":
        score += 4
    if attr(element, "name").lower() in {"q", "query", "keyword", "sword", "search"}:
        score += 4
    for field in ("id", "name", "class", "placeholder", "title", "aria-label"):
        if contains_search_term(attr(element, field)):
            score += 2
    form = ancestor(elements, int(element["index"]), "form")
    if form and contains_search_term(haystack(form, attr(form, "action"))):
        score += 2
    return score


def button_score(elements: list[dict[str, Any]], element: dict[str, Any]) -> int:
    tag = element.get("tag")
    input_type = attr(element, "type").lower()
    if element_is_hidden(elements, element) or element_is_disabled_or_readonly(elements, element):
        return 0
    if tag == "button":
        if input_type and input_type not in {"submit", "button"}:
            return 0
        score = 2 if input_type in {"", "submit"} else 1
    elif tag == "input" and input_type in {"submit", "image", "button"}:
        score = 2
    else:
        return 0
    text = element_text(elements, int(element["index"]))
    for field in ("id", "name", "class", "title", "aria-label", "value"):
        if contains_search_term(attr(element, field)):
            score += 2
    if contains_search_term(text):
        score += 3
    return score


def css_quote(value: str) -> str:
    return '"' + str(value).replace("\\", "\\\\").replace('"', '\\"') + '"'


def selector_for(elements: list[dict[str, Any]], element: dict[str, Any], qualify_form: bool = True) -> str:
    tag = str(element.get("tag") or "")
    form = ancestor(elements, int(element["index"]), "form")
    element_id = attr(element, "id")
    if element_id:
        selector = f"#{element_id}"
    elif attr(element, "name"):
        selector = f"{tag}[name={css_quote(attr(element, 'name'))}]"
    elif attr(element, "class"):
        first_class = attr(element, "class").split()[0]
        selector = f"{tag}.{first_class}"
    elif form and attr(form, "id") and tag == "button" and (attr(element, "type").lower() in {"", "submit"}):
        selector = 'button[type="submit"]'
    elif form and attr(form, "id") and tag == "input" and attr(element, "type").lower() in {"submit", "image", "button"}:
        selector = f'input[type="{attr(element, "type").lower()}"]'
    else:
        selector = tag
    if qualify_form and form and attr(form, "id") and (element_id or selector.startswith(("button[", "input["))):
        return f"#{attr(form, 'id')} {selector}"
    return selector


def find_search_candidates(elements: list[dict[str, Any]]) -> list[dict[str, Any]]:
    candidates = []
    for element in elements:
        score = input_score(elements, element)
        if score < 3:
            continue
        form = ancestor(elements, int(element["index"]), "form")
        button_pool = descendants(elements, int(form["index"])) if form else elements[int(element["index"]) + 1 : int(element["index"]) + 12]
        buttons = [
            {"element": button, "score": button_score(elements, button)}
            for button in button_pool
            if button_score(elements, button) > 0
        ]
        buttons.sort(key=lambda item: (-item["score"], int(item["element"]["index"])))
        best_button = buttons[0]["element"] if buttons else None
        candidates.append(
            {
                "score": score,
                "input": {
                    "tag": element["tag"],
                    "id": attr(element, "id"),
                    "name": attr(element, "name"),
                    "type": attr(element, "type") or "text",
                    "placeholder": attr(element, "placeholder"),
                    "selector": selector_for(elements, element),
                },
                "submit": {
                    "tag": best_button["tag"],
                    "id": attr(best_button, "id"),
                    "type": attr(best_button, "type") or "submit",
                    "text": element_text(elements, int(best_button["index"])),
                    "selector": selector_for(elements, best_button),
                }
                if best_button
                else None,
                "form": {
                    "id": attr(form, "id"),
                    "name": attr(form, "name"),
                    "action": attr(form, "action"),
                    "selector": selector_for(elements, form, qualify_form=False),
                }
                if form
                else None,
            }
        )
    candidates.sort(key=lambda item: (-int(item["score"]), item["input"]["selector"]))
    return candidates


def duplicate_id_counts(elements: list[dict[str, Any]]) -> dict[str, int]:
    counts: dict[str, int] = {}
    for element in elements:
        element_id = attr(element, "id")
        if not element_id:
            continue
        counts[element_id] = counts.get(element_id, 0) + 1
    return {element_id: count for element_id, count in sorted(counts.items()) if count > 1}


def selector_id_references(selector: str) -> list[str]:
    text = str(selector or "")
    ids = re.findall(r"#([^\s>+~,\[.#]+)", text)
    return sorted(set(ids))


def selector_duplicate_id_refs(selector: str, duplicate_ids: dict[str, int]) -> dict[str, int]:
    return {
        element_id: duplicate_ids[element_id]
        for element_id in selector_id_references(selector)
        if element_id in duplicate_ids
    }


def selector_parts(selector: str) -> list[str]:
    text = str(selector or "").strip()
    if not text or any(token in text for token in [">", "+", "~", ","]):
        return []
    return [part for part in text.split() if part]


def selector_match_count(elements: list[dict[str, Any]], selector: str) -> int:
    parts = selector_parts(selector)
    if not parts:
        return 0
    return sum(1 for element in elements if element_matches_selector_chain(elements, int(element["index"]), parts))


def element_matches_selector_chain(elements: list[dict[str, Any]], index: int, parts: list[str]) -> bool:
    if not simple_selector_matches(elements[index], parts[-1]):
        return False
    parent = elements[index].get("parent")
    for part in reversed(parts[:-1]):
        while parent is not None and not simple_selector_matches(elements[parent], part):
            parent = elements[parent].get("parent")
        if parent is None:
            return False
        parent = elements[parent].get("parent")
    return True


def simple_selector_matches(element: dict[str, Any], selector: str) -> bool:
    text = str(selector or "").strip()
    if not text:
        return False
    attrs = element.get("attrs", {})
    selector_without_attrs = re.sub(r"\[[^\]]+\]", "", text)
    id_match = re.search(r"#([^\s>+~,\[.#]+)", selector_without_attrs)
    class_matches = re.findall(r"\.([^\s>+~,\[.#]+)", selector_without_attrs)
    tag = re.split(r"[#.]", selector_without_attrs, maxsplit=1)[0].strip().lower()
    if tag and element.get("tag") != tag:
        return False
    if id_match and attrs.get("id") != id_match.group(1):
        return False
    classes = set(str(attrs.get("class") or "").split())
    if any(class_name not in classes for class_name in class_matches):
        return False
    for attr_name, raw_value in re.findall(r"\[([^\]=\s]+)=((?:\"[^\"]*\")|(?:'[^']*')|[^\]]+)\]", text):
        expected = raw_value.strip().strip("\"'")
        if str(attrs.get(attr_name.lower()) or "") != expected:
            return False
    return True


def add_candidate_selector_evidence(
    candidates: list[dict[str, Any]],
    elements: list[dict[str, Any]],
    duplicate_ids: dict[str, int],
) -> list[dict[str, Any]]:
    for candidate in candidates:
        duplicate_refs = {}
        selector_match_counts = {}
        selector_multiple_matches = {}
        for key in ("input", "submit", "form"):
            selector = str((candidate.get(key) or {}).get("selector") or "")
            refs = selector_duplicate_id_refs(selector, duplicate_ids)
            if refs:
                duplicate_refs[key] = refs
            if selector:
                match_count = selector_match_count(elements, selector)
                selector_match_counts[key] = match_count
                if key in {"input", "submit"} and match_count > 1:
                    selector_multiple_matches[key] = match_count
        candidate["selector_duplicate_ids"] = duplicate_refs
        candidate["selector_match_counts"] = selector_match_counts
        candidate["selector_multiple_matches"] = selector_multiple_matches
        blocking_duplicate_refs = {
            key: value for key, value in duplicate_refs.items() if key in {"input", "submit"}
        }
        candidate["selector_blocking_duplicate_ids"] = blocking_duplicate_refs
        candidate["selectors_unique"] = not bool(blocking_duplicate_refs or selector_multiple_matches)
    return candidates


def url_origin(value: str) -> str:
    parsed = urlparse(str(value or "").strip())
    if not parsed.scheme or not parsed.hostname:
        return ""
    try:
        port = parsed.port
    except ValueError:
        return ""
    default_port = (parsed.scheme == "https" and port == 443) or (parsed.scheme == "http" and port == 80)
    port_text = "" if port is None or default_port else f":{port}"
    return f"{parsed.scheme}://{parsed.hostname}{port_text}"


def is_local_or_link_host(hostname: str | None) -> bool:
    return is_non_public_host(hostname)


def https_url_problem(value: str, *, field_name: str, allow_query: bool = False) -> str:
    text = str(value or "").strip()
    if not text:
        return f"{field_name} is required"
    if any(char.isspace() or ord(char) < 32 or ord(char) == 127 or char == "\\" for char in text):
        return f"{field_name} must not contain whitespace, control characters, or backslashes"
    parsed = urlparse(text)
    try:
        parsed.port
    except ValueError:
        return f"{field_name} must include a valid port"
    if parsed.scheme.lower() != "https" or not parsed.netloc or not parsed.hostname:
        return f"{field_name} must be an absolute HTTPS URL"
    if parsed.username is not None or parsed.password is not None:
        return f"{field_name} must not include credentials"
    if parsed.params or parsed.fragment or (parsed.query and not allow_query):
        return f"{field_name} must not include params, query, or fragment"
    if is_local_or_link_host(parsed.hostname):
        return f"{field_name} must not use non-public hosts"
    return ""


def parse_csp_directives(policy: str) -> dict[str, list[str]]:
    directives: dict[str, list[str]] = {}
    for raw_directive in str(policy or "").split(";"):
        parts = [part for part in raw_directive.strip().split() if part]
        if not parts:
            continue
        directives[parts[0].lower()] = parts[1:]
    return directives


def csp_script_sources(policy: str) -> list[str]:
    directives = parse_csp_directives(policy)
    return directives.get("script-src") or directives.get("default-src") or []


def csp_script_element_sources(policy: str) -> tuple[str, list[str]]:
    directives = parse_csp_directives(policy)
    for name in ("script-src-elem", "script-src", "default-src"):
        if name in directives:
            return name, directives[name]
    return "", []


def csp_inline_script_risky(sources: list[str]) -> bool:
    if not sources:
        return False
    return not any(
        token == "'unsafe-inline'" or token.startswith("'nonce-") or token.startswith("'sha")
        for token in sources
    )


def csp_host_matches(source_host: str, target_host: str) -> bool:
    source = source_host.strip().lower()
    target = target_host.strip().lower()
    if not source or not target:
        return False
    if source == target:
        return True
    if source.startswith("*."):
        suffix = source[1:]
        return target.endswith(suffix) and target != source[2:]
    return False


def csp_source_allows_widget_src(source: str, widget_src: str, page_url: str = "") -> bool:
    return csp_source_allows_target_url(source, widget_src, page_url)


def csp_source_allows_target_url(source: str, target_url: str, page_url: str = "") -> bool:
    token = str(source or "").strip()
    if not token or token in {"'none'", "'unsafe-inline'", "'unsafe-eval'", "'strict-dynamic'"}:
        return False
    if token.startswith("'nonce-") or token.startswith("'sha"):
        return False
    target = urlparse(target_url)
    if not target.scheme or not target.hostname:
        return False
    try:
        target_port = target.port
    except ValueError:
        return False
    if token == "*":
        return True
    if token == "https:":
        return target.scheme == "https"
    if token == "http:":
        return target.scheme == "http"
    if token == "'self'":
        return bool(page_url) and url_origin(page_url) == url_origin(target_url)
    if token.startswith("//"):
        token = f"{target.scheme}:{token}"
    if re.match(r"^https?://", token, re.IGNORECASE):
        parsed = urlparse(token)
        try:
            parsed_port = parsed.port
        except ValueError:
            return False
        if parsed.scheme and parsed.scheme != target.scheme:
            return False
        if not csp_host_matches(parsed.hostname or "", target.hostname or ""):
            return False
        if parsed_port is not None and parsed_port != target_port:
            return False
        source_path = parsed.path.rstrip("/")
        if source_path and source_path != "*":
            target_path = target.path.rstrip("/")
            return target_path == source_path or target_path.startswith(source_path + "/")
        return True
    if token.startswith("*.") or re.match(r"^[\w.-]+(?::\d+)?$", token):
        host_part, _, port_part = token.partition(":")
        expected_port = target_port or (443 if target.scheme == "https" else 80)
        if port_part and str(expected_port) != port_part:
            return False
        return csp_host_matches(host_part, target.hostname or "")
    return False


def csp_widget_src_allowed(policies: list[str], widget_src: str, page_url: str = "") -> bool:
    return not csp_widget_src_blocking_directives(policies, widget_src, page_url)


def csp_widget_src_blocking_directives(policies: list[str], widget_src: str, page_url: str = "") -> list[str]:
    blocking = []
    for policy in policies:
        directive_name, sources = csp_script_element_sources(policy)
        if sources and not any(csp_source_allows_widget_src(source, widget_src, page_url) for source in sources):
            blocking.append(directive_name or "script-src")
    return blocking


def csp_connect_sources(policy: str) -> tuple[str, list[str]]:
    directives = parse_csp_directives(policy)
    for name in ("connect-src", "default-src"):
        if name in directives:
            return name, directives[name]
    return "", []


def csp_api_connect_blocking_directives(policies: list[str], api_base_url: str, page_url: str = "") -> list[str]:
    blocking = []
    target = str(api_base_url or "").rstrip("/")
    if not target:
        return blocking
    for policy in policies:
        directive_name, sources = csp_connect_sources(policy)
        if sources and not any(csp_source_allows_target_url(source, target, page_url) for source in sources):
            blocking.append(directive_name or "connect-src")
    return blocking


def csp_reports_inline_script_risk(
    elements: list[dict[str, Any]],
    *,
    response_policies: list[str] | None = None,
    widget_src: str = "",
    api_base_url: str = "",
    page_url: str = "",
) -> dict[str, Any]:
    policies = []
    for element in elements:
        if element.get("tag") != "meta":
            continue
        if attr(element, "http-equiv").lower() != "content-security-policy":
            continue
        content = attr(element, "content")
        if content:
            policies.append(content)
    policies.extend(str(policy) for policy in (response_policies or []) if str(policy).strip())
    risky = False
    inline_blocking_directives = []
    for policy in policies:
        directive_name, sources = csp_script_element_sources(policy)
        if csp_inline_script_risky(sources):
            risky = True
            inline_blocking_directives.append(directive_name or "script-src")
    widget_src_text = str(widget_src or "").strip()
    widget_blocking_directives = (
        csp_widget_src_blocking_directives(policies, widget_src_text, page_url) if widget_src_text else []
    )
    widget_allowed = not widget_blocking_directives
    api_base_url_text = str(api_base_url or "").rstrip("/")
    api_connect_blocking_directives = (
        csp_api_connect_blocking_directives(policies, api_base_url_text, page_url) if api_base_url_text else []
    )
    api_connect_allowed = not api_connect_blocking_directives
    return {
        "inline_init_risky": risky,
        "inline_init_blocking_directives": sorted(set(inline_blocking_directives)),
        "external_widget_src_risky": bool(widget_src_text and not widget_allowed),
        "widget_src_blocking_directives": sorted(set(widget_blocking_directives)),
        "widget_src_allowed": widget_allowed,
        "widget_src": widget_src_text,
        "widget_src_origin": url_origin(widget_src_text),
        "api_connect_risky": bool(api_base_url_text and not api_connect_allowed),
        "api_connect_blocking_directives": sorted(set(api_connect_blocking_directives)),
        "api_connect_allowed": api_connect_allowed,
        "api_base_url": api_base_url_text,
        "api_base_url_origin": url_origin(api_base_url_text),
        "page_origin": url_origin(page_url),
        "policies": policies,
    }


def existing_widget_report(elements: list[dict[str, Any]], api_base_url: str) -> dict[str, Any]:
    scripts = []
    for element in elements:
        if element.get("tag") != "script":
            continue
        source = attr(element, "src")
        text = str(element.get("text") or "")
        data_auto_init = element.get("attrs", {}).get("data-hai-auto-init")
        if "widget.js" in source or "HaeorumAISearch" in text or data_auto_init is not None:
            scripts.append(
                {
                    "src": source,
                    "relative_src": bool(source and not re.match(r"^https?://", source, re.IGNORECASE)),
                    "data_auto_init": html_truthy_attr(data_auto_init),
                    "manual_init": "HaeorumAISearch" in text and ".init" in text,
                    "api_base_url_marker": bool(api_base_url and api_base_url.rstrip("/") in (source + text)),
                }
            )
    return {
        "found": bool(scripts),
        "scripts": scripts,
        "relative_widget_script": any(item["relative_src"] for item in scripts if "widget.js" in item["src"]),
    }


def html_truthy_attr(value: str) -> bool:
    text = str(value or "").strip().lower()
    return bool(value is not None and (text == "" or text in {"1", "true", "yes", "y", "on"}))


def build_data_auto_init_snippet(
    *,
    widget_src: str,
    mall_id: str,
    api_key: str,
    attach_to_search_input: str,
    attach_after_selector: str,
    trigger_title: str,
    fallback_floating: bool = False,
) -> str:
    attrs = [
        ("src", widget_src),
        ("data-hai-auto-init", "true"),
        ("data-mall-id", mall_id),
        ("data-api-key", api_key),
        ("data-attach-to-search-input", attach_to_search_input),
        ("data-attach-after-selector", attach_after_selector),
        ("data-fallback-floating", "true" if fallback_floating else ""),
        ("data-trigger-title", trigger_title),
    ]
    lines = ["<script"]
    for name, value in attrs:
        if value:
            lines.append(f"  {name}=\"{html.escape(str(value), quote=True)}\"")
    lines[-1] = lines[-1] + "></script>"
    return "\n".join(lines)


