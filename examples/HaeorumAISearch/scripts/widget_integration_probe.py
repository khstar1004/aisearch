def inject_snippet_preview_html(body: str, snippet: str) -> str:
    marker = f"\n<!-- {PREVIEW_INSERTION_MARKER} -->\n"
    insertion = marker + snippet.strip() + "\n"
    text = str(body or "")
    lower = text.lower()
    for closing_tag in ("</body>", "</html>"):
        index = lower.rfind(closing_tag)
        if index >= 0:
            return text[:index] + insertion + text[index:]
    return text.rstrip() + insertion


def write_preview_file(
    *,
    target: Path,
    index: int,
    base: str,
    page: dict[str, Any],
    snippet: str,
) -> str:
    source = str(page.get("source") or page.get("path") or "").strip()
    if not snippet or not source or re.match(r"^https?://", source, re.IGNORECASE):
        return ""
    source_path = Path(source)
    if not source_path.exists() or not source_path.is_file():
        return ""
    try:
        body = source_path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return ""
    preview_dir = target / "previews"
    preview_dir.mkdir(parents=True, exist_ok=True)
    preview_path = preview_dir / f"{index:03d}-{base}.preview.html"
    preview_path.write_text(inject_snippet_preview_html(body, snippet) + "\n", encoding="utf-8")
    return str(preview_path)


def file_sha256(path: Path) -> dict[str, Any]:
    raw = path.read_bytes()
    return {"algorithm": "sha256", "digest": hashlib.sha256(raw).hexdigest(), "size_bytes": len(raw)}


def validate_preview_html_body(
    body: str,
    snippet: str,
    *,
    page_index: Any = None,
    name: Any = None,
    mall_id: str = "",
    preview_file: str = "",
    snippet_file: str = "",
    source_file: str = "",
    attach_to_search_input: str = "",
    attach_after_selector: str = "",
    fallback_floating: bool = False,
    preview_fingerprint: Mapping[str, Any] | None = None,
) -> dict[str, Any]:
    problems = []
    expected_selectors = {
        "attach_to_search_input": str(attach_to_search_input or "").strip(),
        "attach_after_selector": str(attach_after_selector or "").strip(),
    }
    details: dict[str, Any] = {
        "page_index": page_index,
        "name": name,
        "mall_id": mall_id or "",
        "preview_file": preview_file,
        "snippet_file": snippet_file,
        "source_file": source_file,
        "exists": True,
        "marker_count": 0,
        "data_auto_init_script_count": 0,
        "snippet_embedded": False,
        "inserted_before_body_close": False,
        "fallback_floating": bool(fallback_floating),
        "expected_selectors": expected_selectors,
        "selector_match_counts": {},
        "selectors_ok": True,
        "preview_fingerprint": dict(preview_fingerprint or {}),
        "problems": problems,
    }
    marker_count = str(body or "").count(PREVIEW_INSERTION_MARKER)
    details["marker_count"] = marker_count
    if marker_count != 1:
        problems.append(f"preview_marker_count:{marker_count}")
    elements = parse_html(body)
    data_auto_init_count = sum(
        1
        for element in elements
        if element.get("tag") == "script" and html_truthy_attr((element.get("attrs") or {}).get("data-hai-auto-init"))
    )
    details["data_auto_init_script_count"] = data_auto_init_count
    if data_auto_init_count < 1:
        problems.append("data_auto_init_script_missing")
    if data_auto_init_count > 1:
        problems.append(f"data_auto_init_script_multiple:{data_auto_init_count}")
    selector_match_counts: dict[str, int] = {}
    if not fallback_floating:
        for key, selector in expected_selectors.items():
            if not selector:
                continue
            match_count = selector_match_count(elements, selector)
            selector_match_counts[key] = match_count
            if match_count == 0:
                problems.append(f"{key}_selector_missing")
            elif match_count > 1:
                problems.append(f"{key}_selector_multiple:{match_count}")
    details["selector_match_counts"] = selector_match_counts
    details["selectors_ok"] = not any(
        problem.endswith("_selector_missing") or "_selector_multiple:" in problem for problem in problems
    )
    snippet_text = str(snippet or "").strip()
    details["snippet_embedded"] = bool(snippet_text and snippet_text in str(body or ""))
    if not details["snippet_embedded"]:
        problems.append("snippet_not_embedded")
    lower = str(body or "").lower()
    marker_index = str(body or "").find(PREVIEW_INSERTION_MARKER)
    body_close_index = lower.rfind("</body>")
    details["inserted_before_body_close"] = bool(marker_index >= 0 and (body_close_index < 0 or marker_index < body_close_index))
    if not details["inserted_before_body_close"]:
        problems.append("preview_marker_not_before_body_close")
    return {**details, "ok": not problems}


def preview_validation_entry(output: dict[str, Any]) -> dict[str, Any]:
    preview_file = str(output.get("preview_file") or "")
    snippet_file = str(output.get("snippet_file") or "")
    source_file = str(output.get("source_file") or "")
    problems = []
    details: dict[str, Any] = {
        "page_index": output.get("page_index"),
        "name": output.get("name"),
        "mall_id": output.get("mall_id") or "",
        "preview_file": preview_file,
        "snippet_file": snippet_file,
        "source_file": source_file,
        "exists": False,
        "marker_count": 0,
        "data_auto_init_script_count": 0,
        "snippet_embedded": False,
        "inserted_before_body_close": False,
        "fallback_floating": bool(output.get("fallback_floating")),
        "expected_selectors": {
            "attach_to_search_input": str(output.get("attach_to_search_input") or ""),
            "attach_after_selector": str(output.get("attach_after_selector") or ""),
        },
        "selector_match_counts": {},
        "selectors_ok": False,
        "preview_fingerprint": {},
        "problems": problems,
    }
    if not preview_file:
        problems.append("preview_file_not_generated")
        return {**details, "ok": False}
    preview_path = Path(preview_file)
    if not preview_path.exists() or not preview_path.is_file():
        problems.append("preview_file_missing")
        return {**details, "ok": False}
    try:
        body = preview_path.read_text(encoding="utf-8", errors="replace")
        preview_fingerprint = file_sha256(preview_path)
    except OSError as exc:
        problems.append(f"preview_file_unreadable:{type(exc).__name__}")
        return {**details, "ok": False}
    snippet = ""
    snippet_problem = ""
    if snippet_file and Path(snippet_file).exists():
        try:
            snippet = Path(snippet_file).read_text(encoding="utf-8").strip()
        except OSError as exc:
            snippet_problem = f"snippet_file_unreadable:{type(exc).__name__}"
    else:
        snippet_problem = "snippet_file_missing"
    validation = validate_preview_html_body(
        body,
        snippet,
        page_index=output.get("page_index"),
        name=output.get("name"),
        mall_id=output.get("mall_id") or "",
        preview_file=preview_file,
        snippet_file=snippet_file,
        source_file=source_file,
        attach_to_search_input=str(output.get("attach_to_search_input") or ""),
        attach_after_selector=str(output.get("attach_after_selector") or ""),
        fallback_floating=bool(output.get("fallback_floating")),
        preview_fingerprint=preview_fingerprint,
    )
    if snippet_problem:
        validation["problems"] = [
            problem for problem in validation.get("problems", []) if problem != "snippet_not_embedded"
        ] + [snippet_problem]
        validation["snippet_embedded"] = False
        validation["ok"] = False
    return validation


